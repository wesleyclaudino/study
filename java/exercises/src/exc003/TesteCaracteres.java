package exc003;

public class TesteCaracteres {
	
	public static void main(String[] args) {
		
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String palavra = "Curso de Java";
		System.out.println(palavra);
		
		palavra = palavra + 2021;
		System.out.println(palavra);
		
	}
}